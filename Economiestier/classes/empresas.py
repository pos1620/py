class empresas:
    nome=None;
    endereco=None;
    email=None;
    phone=None;



    def setNome(self,nome):
        self.nome=nome;

    def getNome(self):
        return self.nome;


    def setEndereco(self,endereco):
        self.endereco=endereco;

    def getEndereco(self):
        return self.endereco;

    def setEmail(self,email):
        self.email=email;

    def getEmail(self):
        return self.email;

    def setPhone(self,phone):
        self.phone=phone;

    def getPhone(self):
        return self.phone;
